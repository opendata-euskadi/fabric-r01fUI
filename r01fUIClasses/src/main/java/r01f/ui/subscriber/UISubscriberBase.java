package r01f.ui.subscriber;

public abstract class UISubscriberBase<T> 
           implements UISubscriber<T> {
/////////////////////////////////////////////////////////////////////////////////////////
//  FIELDS
/////////////////////////////////////////////////////////////////////////////////////////	
	protected final UISubscriber<?> _subscriber;
/////////////////////////////////////////////////////////////////////////////////////////
//  CONSTRUCTOR
/////////////////////////////////////////////////////////////////////////////////////////
	public UISubscriberBase(final UISubscriber<?> subscriber) {
		_subscriber = subscriber;
	}
/////////////////////////////////////////////////////////////////////////////////////////
//  
/////////////////////////////////////////////////////////////////////////////////////////
	@Override
	public void onError(final Throwable th) {
		if (_subscriber != null) _subscriber.onError(th);
	}
}
