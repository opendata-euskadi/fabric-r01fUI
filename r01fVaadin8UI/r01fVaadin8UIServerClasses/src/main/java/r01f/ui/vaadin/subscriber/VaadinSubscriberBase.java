package r01f.ui.vaadin.subscriber;

public abstract class VaadinSubscriberBase<T> 
           implements VaadinSubscriber<T> {
/////////////////////////////////////////////////////////////////////////////////////////
//  FIELDS
/////////////////////////////////////////////////////////////////////////////////////////	
	protected final VaadinSubscriber<?> _subscriber;
/////////////////////////////////////////////////////////////////////////////////////////
//  CONSTRUCTOR
/////////////////////////////////////////////////////////////////////////////////////////
	public VaadinSubscriberBase(final VaadinSubscriber<?> subscriber) {
		_subscriber = subscriber;
	}
/////////////////////////////////////////////////////////////////////////////////////////
//  
/////////////////////////////////////////////////////////////////////////////////////////
	@Override
	public void onError(final Throwable th) {
		if (_subscriber != null) _subscriber.onError(th);
	}
}
